#include<iostream>
#include<math.h>
using namespace std;
/*
 ceil()
 cos()
 exp()
 fabs()
 floor()
 log()
 log10()
 pow(x,y)
 sin()
 sqrt()
 tan()
 */
int main()
{
	cout<<"pow="<<pow(2,3)<<endl;
	cout<<"ceil="<<ceil(2.6)<<endl;
	cout<<"floor="<<floor(2.3)<<endl;
	cout<<"cos="<<cos(2.8)<<endl;
	cout<<"exp"<<exp(2.3)<<endl;
	cout<<"fabs="<<fabs(2)<<endl;
	cout<<"log="<<log(2)<<endl;
	cout<<"log10="<<log10(2)<<endl;
	cout<<"sin="<<sin(2.6)<<endl;
	cout<<"sqrt="<<sqrt(12)<<endl;
	cout<<"tan="<<tan(2)<<endl;
	return 0;
}
